	if eventToCheck_ == PSG.BTN_QUIT:
		CF.exit()

	elif eventToCheck_ == PSG.CHECKBOX_RUNAWAY:
		PSG.MAPPDS[PSG.CHECKBOX_RUNAWAY] = not PSG.MAPPDS[PSG.CHECKBOX_RUNAWAY]

	elif eventToCheck_ == PSG.CHECKBOX_ALPHA_LOW:
		PSG.MAPPDS[PSG.CHECKBOX_ALPHA_LOW] = not PSG.MAPPDS[PSG.CHECKBOX_ALPHA_LOW]

